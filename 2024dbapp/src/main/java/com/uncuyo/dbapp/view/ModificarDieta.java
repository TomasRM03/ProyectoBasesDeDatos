    private void jButton3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton3MouseClicked
        try {
            Integer id2 = Integer.valueOf(id.getText());
            Dieta dieta = mainframe.getDieta(id2);
            objetivo.setText(dieta.getObjetivodieta());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "No se ha encontrado el ID, reintente", "Error", JOptionPane.ERROR_MESSAGE);
        }
               
    }//GEN-LAST:event_jButton3MouseClicked

    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        String obj = objetivo.getText();
        try {
            Integer id2 = Integer.valueOf(id.getText());
            mainframe.modificarDieta(id2, obj);
            this.dispose();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "No se ha encontrado el ID, reintente", "Error", JOptionPane.ERROR_MESSAGE);
        }    
        
    }//GEN-LAST:event_jButton1MouseClicked

